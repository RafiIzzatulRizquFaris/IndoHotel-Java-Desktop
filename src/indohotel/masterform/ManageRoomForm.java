    private void table_roomMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_roomMousePressed
        int i = mu.getSelectedRow(table_room);
        id = dtm.getValueAt(i, 0).toString();
        tf_room_number.setText(dtm.getValueAt(i, 2).toString());
        tf_floor.setText(dtm.getValueAt(i, 3).toString());
        String roomType = dtm.getValueAt(i, 1).toString();
        switch(roomType){
            case "Economic":
                cb_room_type.setSelectedIndex(0);
                break;
            case "Medium Sized":
                cb_room_type.setSelectedIndex(1);
                break;
            case "Luxury":
                cb_room_type.setSelectedIndex(2);
                break;
        }
    }//GEN-LAST:event_table_roomMousePressed


