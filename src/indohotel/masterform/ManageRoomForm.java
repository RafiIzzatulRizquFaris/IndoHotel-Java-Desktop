    private void showData() {
        int row = table_room.getRowCount();
        for (int i = 0; i < row; i++) {
            dtm.removeRow(0);
        }
        
        try{
            Statement st = conn.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery("SELECT kamar.id_kamar, jeniskamar.nama_jenis_kamar, kamar.nomor_kamar, kamar.lantai_kamar FROM kamar, jeniskamar WHERE kamar.id_jenis_kamar = jeniskamar.id_jenis_kamar");
            while (rs.next()) {
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageRoomForm.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.toString());
        }
    }
    
    public void clearField(){
        tf_floor.setText(null);
        tf_room_number.setText(null);
        cb_room_type.setSelectedIndex(0);
    }
    
    private String getComboBox(){
        String roomType = "";
        switch(cb_room_type.getSelectedIndex()){
            case 0:
                roomType = "1";
                break;
            case 1:
                roomType = "2";
                break;
            case 2:
                roomType = "3";
                break;
        }
        return roomType;
    }
